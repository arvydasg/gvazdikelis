When I am learning without a plan or an idea of what I am doing, I get in this mode of constant kind of anxiety, feeling that I can ONLY spend my time of the things I am trying to learn, that god forbid I will waste some time by washing clothes or getting a shave or something - all that seems like a waste of time.<br><br> 

But now, since I am more relaxed, following the pomodoro technique, ticking off things from my sunday to-do list, it is only 11:30 and I have already done a lot of things that help to keep my days(life) in balance. AND at the same time I still have loads of time to do HOURS of learning. Productive time, with  calm mind, because other things are sorted and not at the back of my mind.<br><br>

