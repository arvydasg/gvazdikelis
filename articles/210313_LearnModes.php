<h2>Focused mode</h2>
        


<p>Focused mode - like paintball. Think of something, the thought goes from one neuron to the other. As it is bouncing on the bumpers, you are able to solve a problem of something you are rather <strong>familiar with</strong>. Thought moves smoothly along the already existing neural pathway - moving on a familiar, nicely paved road.</p>

<p>Trying to do a multiplication problem or looking for a certain word in your mind - probably stepping along the familiar pathways of the focused mode.</p>

<h2>Difuse mode</h2>


<p>Diffused thinking - relaxed. You haven't thought before - you dont know where the pattern\path is. All the other neurons are blocking the patterns. New neural pathways have a way to appear, because you don't bump into anything. (diffused mode).</p>


<p>If you are trying to solve a new problem it often cries out for the more broad ranging perspectives of the diffuse mode.</p>

