        <p>The diffuse mode could be thought of as a flashlight set so that it casts its light very broadly, but not very strongly in any one area. (As opposed to the focused mode, which would have its light cast very strongly in a single area, but very weakly everywhere else). Focused - The type of thinking you need to do when you are trying to understand something new.</p>



