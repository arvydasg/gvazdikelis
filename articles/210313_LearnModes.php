<h2>Can't have both</h2>

<p>When we find ourselves stuck on a problem, or even if we’re unsure of a situation, the course of living our daily life. It's often a good idea once you’ve focused directly on the situation <strong>to let things settle back</strong> and take a bit more time. That way more neural processing can take place, often below conscious awareness in the diffuse mode.</p>

<p>One mode limits the access to the other type of mode. Either one or the other side of the coin, can not have both, same with thinking modes. Either one or the other type of thinking.</p>


