<p><strong>Salvador Dali</strong> - Relaxing in the chair, vaguely thinking what he was previously focusing on, holding keys, falling asleep, then when the keys fall down, he wakes up and then goes to paint with the new connections that he has just made while in the diffuse mode.</p>

<p>He suggests that the elements of creativity process are Preparation– know your domain, Concentration– intense focus, Incubation– relax and do something the polar opposite, Illumination– breakthrough on the ideas and Verification– test the idea.</p>

